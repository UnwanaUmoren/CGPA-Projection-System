    	<h1>System Overview</h1>
        <p>CGPA Projection system is a web based system designed for tetiary institution. It has a simple GUI for easy usage by users, with cool features.</p>
        <h1>System Features</h1>
        <p>The system features are:</p>
        <h2>Authentication</h2>
    	<p id='p2'>Users are required to login before using the system tools.</p>
        <h2>Course management</h2>
    	<p id='p2'>Registered users can register courses for the active semester, and can also add or drop courses but only after course registration.</p>
        <h2>Result management</h2>
    	<p id='p2'>Registered users can view result of the previous semester or any other level semester result.</p>
        <h2>CGPA projection</h2>
    	<p id='p2'>Registered users can check for courses grade combination of the registered courses that will give him/her the CGPA that falls in CGPA range he/she desires for that semester</p>
        <h2>Profile management</h2>
    	<p id='p2'>Registered users can edit some of their details they provided during registration</p>
  
